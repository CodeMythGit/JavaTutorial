package com.codemyth;

public class FractionalDataType {

	public static void main(String[] args) {
		//Float and Double
		
		float perc = 90f;//upto 6 to 7 digit
		double rate = 3.4d;//15 digits
		
		System.out.println(perc+" : "+rate);
	
		//400000000000000000
		//10 - e
		double num = 4e17;
		System.out.println(num*10);
		
	}

}
