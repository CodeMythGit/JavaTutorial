package com.codemyth;

public class AssignmentOperator {

	public static void main(String[] args) {
		//Assignment operator
		int x = 15;
		
		x = x+10;
		System.out.println(x);
		
		x +=10;
		System.out.println(x);
		
		x -=10;
		System.out.println(x);
//		x -=10;
		
//		x = x+10  ==> x +=10
//		x = x-10 ==> x -=10;
//		x = x*10 ==> x *=10
//		x = x/10 ==> x /=10  
//		x = x%10 ==> x %=10
		
	}

}
